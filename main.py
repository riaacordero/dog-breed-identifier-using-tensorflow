# Start the camera capture
cap = cv2.VideoCapture(0)

while True:
    # Capture frame-by-frame
    ret, frame = cap.read()

    # Resize the image
    frame = cv2.resize(frame, (800, 600))

    # Preprocess the image
    img = cv2.resize(frame, (224, 224))
    img = np.expand_dims(img, axis=0)
    img = img / 255.0

    # Predict the breed
    prediction = model.predict(img)
    breed = labels[np.argmax(prediction)]

    # Display the breed on the frame
    cv2.putText(frame, breed, (10, 30), cv2.FONT_HERSHEY_SIMPLEX, 1, (0, 255, 0), 2)

    # Display the resulting frame
    cv2.imshow('Dog Breed Identifier', frame)

    # Exit if 'q' is pressed or window is closed
    if cv2.waitKey(1) & 0xFF == ord('q') or cv2.getWindowProperty('Dog Breed Identifier', cv2.WND_PROP_VISIBLE) < 1:
        break

# Release the capture and close the window
cap.release()
cv2.destroyAllWindows()

